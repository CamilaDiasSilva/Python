# Capturando o nome do usuário
nome = input("Digite seu nome: ")


# Capturando a idade do usuário
idade = int(input("Digite sua idade: "))


# Calculando a idade daqui a 5 anos
idade_futura = idade + 5


# Exibindo a mensagem
print(f"Olá, {nome}! Em 5 anos você terá {idade_futura} anos.")
